const add = (a, b) => {
    return a + b;
};

const sub = (a, b) => {
    return a - b;
};

const multiply = (a, b) => {
    return a * b;
};

const divide = (a, b) => {
    return a / b;
};

const remainder = (a, b) => {
    return a % b;
};

module.exports = {
    add: add,
    sub: sub,
    multiply: multiply,
    divide: divide,
    remainder: remainder,
};